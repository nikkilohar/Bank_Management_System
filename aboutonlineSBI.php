	<p>State Bank of India is India's largest bank with a network of over 25000 branches located even in the remotest parts of India. State Bank of India (SBI) offers a wide range of banking products and services to corporate and retail customers.</p>

	<p>
		OnlineSBI is the Internet banking portal for State Bank of India. The portal provides anywhere, anytime, online access to accounts for State Bank's Retail and Corporate customers. The application is developed using the latest cutting edge technology and tools. The infrastructure supports unified, secure access to banking services for accounts in over 25,000 branches across India.
        <br>
        The Retail banking application is an integration of several functional areas, and enables customers to: 
	</p>

	<ul>
		<li>Issue Demand Drafts online</li>
		<li>Transfer funds to own and third party accounts</li>
		<li>Credit beneficiary accounts using the VISA Money Transfer, RTGS/NEFT feature</li>
		<li>Generate account statements</li>
		<li>Setup Standing Instructions</li>
		<li>Configure profile settings</li>
		<li>Use eTax for online tax payment</li>
		<li>Use ePay for automatic bill payments</li>
		<li>Interface with merchants for railway and airline reservations</li>
		<li>avail DEMAT and IPO services</li>
	</ul>

	<p>
		The OnlineSBI corporate banking application provides features to administer and manage corporate accounts online. The corporate module provides roles such as Regulator, Admin, Uploader, Transaction Maker, Authorizer, and Auditor. These roles have access to the following functions: 
	</p>

	<ul>
		<li>Manage users, define rights and transaction rules on corporate accounts</li>
		<li>Access accounts in several branches with a single sign-on mechanism</li>
		<li>Upload files to make bulk transactions to third parties, supplier, vendor and tax collection authorities.</li>
		<li>Use online transactional features such as fund transfer to own accounts, third party payments (both Inter and Intra bank), and draft issues</li>
		<li>Make bill payments over the Internet.</li>
		<li>Authorize, modify, reschedule and cancel transactions, based on rights assigned to the user</li>
		<li>Generate account statement</li>
		<li>Enquire on transaction details or current balance</li>
	</ul>

